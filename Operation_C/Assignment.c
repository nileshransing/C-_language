#include <stdio.h>
int main (){
   
   // Assignment =
    int m= 45;
    int n= 12;

    printf(" m = %d \n",m);

    // +=
    m+=5 ;
    printf(" m+= %d \n",m);

     // -=
    n-=5 ;
    printf(" n-= %d \n",n);

     // *=
    m*=5 ;
    printf(" m*= %d \n",m);


     // /=
     n= 66;
    n/=6 ;
    printf(" n/= %d \n",n);

     // %=
     
    n%=5 ;
    printf(" n%= %d \n",n);


    return 0;
}