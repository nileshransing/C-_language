#include <stdio.h>
int main(){

    // +
    int a=24;
    int b=6;
    printf("addtion is = %d \n",a+b);

    //sub
    printf("sub is = %d \n",a-b);

    // multiplication
    printf("mul = %d \n",a*b);

    //divide
    printf("div = %d \n",a/b);

    // increament ++

    printf("With increment is = %d \n",++a);

    // decrement --
    printf("With decrement is = %d \n",--b);


    return 0;
}